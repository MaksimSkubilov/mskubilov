 public class Max {
	/**
	 * max. Сравнение.
	 * @param first - первый аргумент.
	 * @param second - второй аргумент.
	 * @return - returns max of two numbers.
	 */
	public int max(int first, int second) {
		return first > second ? first : second;
	}
}